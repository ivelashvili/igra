"""
Ручной тест начисления доходов
Проверяет округление вручную
"""
import math

def test_manual_rounding():
    """Ручной тест округления"""
    print("\n" + "="*60)
    print("РУЧНОЙ ТЕСТ ОКРУГЛЕНИЯ")
    print("="*60)
    
    # Тестовые значения
    test_values = [2.43, 2.5, 2.7, 2.99, 3.0, 3.01, 3.49, 3.5, 3.99]
    
    print("\nТест math.ceil:")
    for val in test_values:
        rounded = math.ceil(val)
        old_rounded = int(round(val))
        diff = rounded - old_rounded
        print(f"  {val:.2f} -> ceil: {rounded}, round: {old_rounded}, разница: {diff}")
    
    # Проверяем конкретный случай
    print("\nКонкретный случай: 2.43")
    val = 2.43
    rounded = math.ceil(val)
    print(f"  math.ceil(2.43) = {rounded}")
    print(f"  Ожидается: 3")
    
    # Проверяем, может ли быть 4
    print("\nПроверка: может ли быть 4?")
    print(f"  Если начисляется дважды: 2.43 -> ceil(2.43) = 3, затем еще раз 3 + ceil(2.43) = 3 + 3 = 6")
    print(f"  Если округляется неправильно: ceil(2.43 * 2) = ceil(4.86) = 5")
    print(f"  Если начисляется 2.43 + 1.57: ceil(2.43) + ceil(1.57) = 3 + 2 = 5")
    print(f"  Если начисляется 2.43 + 2.43: ceil(2.43) + ceil(2.43) = 3 + 3 = 6")
    
    # Проверяем, может ли быть проблема с несколькими объектами
    print("\nПроверка: несколько объектов?")
    print(f"  Если у игрока 2 объекта Лесоповал:")
    print(f"    Доход за 1 объект: ceil(2.43) = 3")
    print(f"    Доход за 2 объекта: ceil(2.43) * 2 = 3 * 2 = 6")
    print(f"    Но если начисляется за каждый отдельно: 3 + 3 = 6")
    
    # Проверяем, может ли быть проблема с модификатором
    print("\nПроверка: модификатор изменился?")
    print(f"  Если модификатор изменился с 0.81 до 1.0:")
    print(f"    Доход: 3 * 0.81 = 2.43 -> ceil(2.43) = 3")
    print(f"    Доход: 3 * 1.0 = 3.0 -> ceil(3.0) = 3")
    print(f"    Но если модификатор стал 1.33:")
    print(f"    Доход: 3 * 1.33 = 3.99 -> ceil(3.99) = 4")

if __name__ == "__main__":
    test_manual_rounding()

