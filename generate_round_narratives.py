def create_pdf(output_path="тексты_для_гзк.pdf"):
    """Создает PDF с текстами для голоса за кадром"""
    
    # Регистрируем шрифты
    try:
        font_paths = [
            "/System/Library/Fonts/Helvetica.ttc",
            "/Library/Fonts/Arial.ttf",
            "/System/Library/Fonts/Supplemental/Arial.ttf",
        ]
        for font_path in font_paths:
            if os.path.exists(font_path):
                try:
                    pdfmetrics.registerFont(TTFont('UnicodeFont', font_path))
                    break
                except:
                    continue
    except:
        pass
    
    doc = SimpleDocTemplate(output_path, pagesize=A4)
    story = []
    styles = getSampleStyleSheet()
    
    font_name = 'UnicodeFont' if 'UnicodeFont' in pdfmetrics.getRegisteredFontNames() else 'Helvetica'
    
    # Заголовок
    title_style = ParagraphStyle(
        'Title',
        parent=styles['Heading1'],
        fontName=font_name,
        fontSize=24,
        textColor=colors.HexColor('#1a1a1a'),
        spaceAfter=20,
        alignment=1,
        fontStyle='BOLD'
    )
    title = Paragraph("КОРОЛЕВСКАЯ БИРЖА", title_style)
    story.append(title)
    
    subtitle_style = ParagraphStyle(
        'Subtitle',
        parent=styles['Normal'],
        fontName=font_name,
        fontSize=14,
        textColor=colors.HexColor('#666666'),
        spaceAfter=30,
        alignment=1
    )
    subtitle = Paragraph("Тексты для голоса за кадром (ГЗК)", subtitle_style)
    story.append(subtitle)
    story.append(Spacer(1, 20))
    
    # Стили
    round_title_style = ParagraphStyle(
        'RoundTitle',
        parent=styles['Heading2'],
        fontName=font_name,
        fontSize=18,
        textColor=colors.HexColor('#2c3e50'),
        spaceAfter=15,
        spaceBefore=25,
        fontStyle='BOLD'
    )
    
    narrative_style = ParagraphStyle(
        'Narrative',
        parent=styles['Normal'],
        fontName=font_name,
        fontSize=11,
        textColor=colors.HexColor('#34495e'),
        spaceAfter=15,
        leading=16,
        leftIndent=10,
        rightIndent=10
    )
    
    # Обрабатываем каждый раунд
    for round_num in range(1, 11):
        story.append(Paragraph(f"РАУНД {round_num}", round_title_style))
        
        narrative_text = ROUND_NARRATIVES.get(round_num, "Текст не найден")
        story.append(Paragraph(narrative_text, narrative_style))
        
        # Разрыв страницы между раундами (кроме последнего)
        if round_num < 10:
            story.append(PageBreak())
    
    # Примечание
    info_style = ParagraphStyle(
        'Info',
        parent=styles['Normal'],
        fontName=font_name,
        fontSize=9,
        textColor=colors.HexColor('#7f8c8d'),
        spaceAfter=10,
        leftIndent=10,
        rightIndent=10,
        alignment=1
    )
    story.append(Spacer(1, 20))
    story.append(Paragraph(
        "<b>Примечание:</b> Эти тексты предназначены для голоса за кадром в видео, которые будут показывать события каждого раунда. "
        "Тексты интегрируют персонажей игры и создают атмосферу средневекового королевства Тогучин.",
        info_style
    ))
    
    doc.build(story)
    print(f"PDF создан: {output_path}")

if __name__ == "__main__":
    create_pdf("тексты_для_гзк.pdf")

